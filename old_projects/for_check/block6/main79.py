def is_member(m, g, z):
    return m == g * (z ** 0) or (z != 1 and (m * (z - 1)) % (g * (z - 1)) == 0)

# Пример использования
m = 8
g = 2
z = 2

if is_member(m, g, z):
    print(f"{m} является членом геометрической прогрессии с первым членом {g} и знаменателем {z}")
else:
    print(f"{m} не является членом геометрической прогрессии с первым членом {g} и знаменателем {z}")
