def is_fibonacci_number(n):
    if n < 1:
        return False  # Фибоначчи числа начинаются с 1

    a, b = 1, 1  # Первые два члена последовательности
    while a < n:
        a, b = b, a + b  # Генерируем следующие члены последовательности

    return a == n  # Проверяем, равно ли n текущему члену последовательности

# Пример использования
number = int(input("Введите натуральное число: "))
if is_fibonacci_number(number):
    print(f"{number} является членом последовательности Фибоначчи.")
else:
    print(f"{number} не является членом последовательности Фибоначчи.")
