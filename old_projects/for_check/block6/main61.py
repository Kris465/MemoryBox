def find_multiple_of_seven(sequence):
    """
    Находит первое число, кратное семи, в последовательности до появления -1
    
    Args:
        sequence: список целых чисел, оканчивающийся на -1
        
    Returns:
        int или None: порядковый номер первого числа, кратного семи, 
                     или None если такого числа нет
    """
    # Проверяем каждый элемент последовательности
    for i, num in enumerate(sequence):
        # Если нашли -1, прекращаем поиск
        if num == -1:
            break
        # Если число кратно семи, возвращаем его позицию
        if num % 7 == 0:
            return i + 1  # Возвращаем порядковый номер (начиная с 1)
    
    return None  # Если не найдено ни одного числа, кратного семи

# Примеры использования
test_sequences = [
    [14, 21, 5, -1],      # Первое число уже кратно семи
    [4, 9, 28, -1],       # Число кратно семи есть
    [4, 9, 10, -1],       # Чисел кратных семи нет
    [21, 35, 49, -1],     # Все числа кратны семи
]

for seq in test_sequences:
    result = find_multiple_of_seven(seq)
    print(f"\nПоследовательность: {seq}")
    if result:
        print(f"Первое число, кратное семи, находится на позиции {result}")
    else:
        print("В последовательности нет чисел, кратных семи")
