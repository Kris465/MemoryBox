# Шаг 1: Найдем остаток от деления 200 на 17
remainder = 200 % 17

# Шаг 2: Если остаток не равен нулю, добавим недостающее количество
if remainder != 0:
    next_multiple = 200 + (17 - remainder)
else:
    next_multiple = 200

# Шаг 3: Проверим, что найденное число больше 200
if next_multiple > 200:
    print(f"Минимальное число, большее 200, которое делится на 17: {next_multiple}")
else:
    print(f"Число 200 уже делится на 17.")
