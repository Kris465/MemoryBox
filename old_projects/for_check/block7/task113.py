# Запрашиваем ввод всех чисел одновременно
numbers_input = input("Введите 9 вещественных чисел через пробел: ")

# Инициализация переменных для хранения суммы и количества подходящих чисел
total_sum = 0
count = 0

# Обрабатываем каждое введённое число
for number_str in numbers_input.split():
    number = float(number_str)  # Преобразование строки в вещественное число
    if number > 10:
        total_sum += number      # Добавляем число к сумме
        count += 1               # Увеличиваем счётчик подходящих чисел

# Вычисление среднего арифметического
if count > 0:
    average = total_sum / count
    print(f"Среднее арифметическое чисел, больших 10: {average:.2f}")
else:
    print("Нет чисел, больших 10.")
