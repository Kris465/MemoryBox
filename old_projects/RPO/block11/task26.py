# Массив с количеством осадков за каждый день февраля
# Например, можно ввести свои данные или оставить пример
rainfall = [3, 0, 2, 5, 1, 0, 4, 2, 3, 1,
            0, 2, 4, 1, 0, 3, 2, 1, 0, 2,
            3, 1, 0, 2, 4, 1, 0]

# Проверка длины массива (должно быть либо 28 или 29 для високосного)
days_in_february = len(rainfall)

# Расчет среднедневного количества осадков
average_precipitation = sum(rainfall) / days_in_february

print(f"Среднедневное количество осадков \
    в феврале: {average_precipitation:.2f}")
