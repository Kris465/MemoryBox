costs = [120, 95, 110, 130, 85, 100, 115, 90, 105, 125,
         80, 135, 140, 70, 145, 75, 150, 65, 155, 60,
         160, 155, 150, 145, 140, 135, 130, 125, 120, 115]

min_cost = min(costs)

first_index = costs.index(min_cost) + 1

last_index = len(costs) - 1 - costs[::-1].index(min_cost) + 1

print(
    "Порядковый номер самого дешевого вида конфет (первый случай):",
    first_index)
print(
    "Порядковый номер самого дешевого вида конфет (последний случай):",
    last_index)
