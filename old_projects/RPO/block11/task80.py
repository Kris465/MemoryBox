# Ввод роста 30 учеников через пробел
heights = list(map(int, input("Введите рост 30 учеников (в см) через пробел: ")
                   .split()))

# Проверка правильности ввода
if len(heights) != 30:
    print("Ошибка: необходимо ввести ровно 30 значений.")
else:
    # Подсчет количества учеников с ростом больше 170 см
    count_bigger_170 = sum(1 for h in heights if h > 170)
    print("Количество учеников с ростом больше 170 см:", count_bigger_170)

    if count_bigger_170 >= 5:
        print("Можно сформировать баскетбольную команду.")
    else:
        print("Нельзя сформировать баскетбольную команду.")
