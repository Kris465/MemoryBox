# Массив с баллами за каждый из 10 видов спорта
# Замените на свои данные при необходимости
scores = [8.5, 9.0, 7.8, 8.2, 9.1, 8.7, 7.9, 8.3, 9.2, 8.6]

# Известное значение для выхода в следующий этап
threshold = 80

# Вычисляем сумму баллов
total_score = sum(scores)

# Проверяем условие выхода
if total_score > threshold:
    print("Спортсмен вышел в следующий этап соревнований.")
else:
    print("Спортсмен не вышел в следующий этап соревнований.")
