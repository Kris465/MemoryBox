# Ввод массива
arr = list(map(float, input("Введите массив вещественных \
    чисел через пробел: ").split()))

# Ввод параметров a1, b, c
a1 = int(input("Введите номер a1 (начиная с 1): "))
b = float(input("Введите число b: "))
c = float(input("Введите число c: "))

# Проверка корректности номера a1
if not (1 <= a1 <= len(arr)):
    print("Номер a1 выходит за границы массива.")
else:
    idx_a1 = a1 - 1

    # а) Ко всем отрицательным элементам прибавить элемент с номером a1,
    # из всех нулевых вычесть число b.
    # Положительные оставить без изменения.
    for i in range(len(arr)):
        if arr[i] < 0:
            arr[i] += arr[idx_a1]
        elif arr[i] == 0:
            arr[i] -= b
        # положительные элементы остаются без изменений

    # б) Из всех положительных элементов вычесть a,
    # из всех отрицательных — b,
    # ко всем нулевым прибавить c.
    for i in range(len(arr)):
        if arr[i] > 0:
            arr[i] -= c
        elif arr[i] < 0:
            arr[i] -= b
        elif arr[i] == 0:
            arr[i] += c

# Вывод результата
print("Изменённый массив:", arr)
