heights = [180, 175, 170, 165, 160, 155, 150, 145, 140, 135, 
           130, 125, 120, 115, 110, 105, 100, 95, 90, 85, 80, 75, 70, 65, 60]

n = int(input("Введите номер выбывшего ученика (1-25): ")) - 1  # Переводим в индекс

if 0 <= n < len(heights):
    new_heights = heights[:n] + heights[n+1:]  # Удаляем n-й элемент
    print("Новый массив роста:", new_heights)
else:
    print("Ошибка: номер должен быть от 1 до 25!")
