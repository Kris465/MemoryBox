# Вводим трехзначное число n
n = int(input("Введите трехзначное число (100 <= n <= 999): "))

# Проверяем, что введенное число соответствует условию
if 100 <= n <= 999:
    # Извлекаем цифры числа n
    hundreds = n // 100          # Первая цифра
    tens = (n // 10) % 10        # Вторая цифра
    units = n % 10               # Третья цифра

    # Образуем двузначное число без последней цифры
    two_digit_number = hundreds * 10 + tens

    # Приписываем вторую цифру к двузначному числу
    x = two_digit_number * 10 + tens

    print(f"Образованное число m: {x}")

else:
    print("Число не соответствует условиям (должно быть от 100 до 999).")
