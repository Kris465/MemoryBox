const prompt = require('prompt-sync')(); // Импортируем библиотеку prompt-sync

// Рекурсивная функция для нахождения числа Фибоначчи
function fibonacci(n) {
    // Базовые случаи: f(1) = 1 и f(2) = 1
    if (n <= 0) {
        return 0; // Если номер меньше или равен 0, возвращаем 0
    }
    if (n === 1 || n === 2) {
        return 1; // Первые два числа Фибоначчи равны 1
    }
    // Рекурсивный вызов для нахождения числа Фибоначчи
    return fibonacci(n - 1) + fibonacci(n - 2);
}

// Получаем пользовательский ввод
const input = prompt('Введите порядковый номер числа Фибоначчи: ');

// Проверяем, является ли введенное значение числом
if (isNaN(input) || input.trim() === '') {
    console.log('Пожалуйста, введите корректное число.');
} else {
    const number = parseInt(input, 10); // Преобразуем строку в число
    const result = fibonacci(number); // Вызываем рекурсивную функцию
    console.log(`Число Фибоначчи с порядковым номером ${number}: ${result}`);
}
