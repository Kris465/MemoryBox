const prompt = require('prompt-sync')(); // Импортируем библиотеку prompt-sync

// Рекурсивная функция для нахождения наибольшего общего делителя
function gcd(a, b) {
    if (b === 0) {
        return a; // Базовый случай: если b равно 0, то a - это НОД
    }
    return gcd(b, a % b); // Рекурсивный случай
}

// Получаем пользовательский ввод для двух чисел
const aInput = prompt('Введите первое число: ');
const bInput = prompt('Введите второе число: ');

// Преобразуем введенные значения в числа
const a = parseInt(aInput, 10);
const b = parseInt(bInput, 10);

// Проверяем, являются ли оба значения целыми числами
if (isNaN(a) || isNaN(b)) {
    console.log('Пожалуйста, введите корректные целые числа.');
} else {
    // Вызываем функцию и выводим результат
    const result = gcd(a, b);
    console.log(`Наибольший общий делитель ${a} и ${b} равен ${result}`);
}
