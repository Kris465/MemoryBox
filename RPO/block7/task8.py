# Ввод числа n
n = int(input("Введите число n (0 < n <= 27): "))

# Проверка корректности ввода
if n <= 0 or n > 27:
    print("Ошибка: число n должно быть в диапазоне 0 < n <= 27.")
else:
    # Инициализация счетчика
    count = 0

    # Перебираем трехзначные числа
    for number in range(100, 1000):
        # Вычисляем сумму цифр числа
        digit_sum = sum(int(digit) for digit in str(number))
        # Если сумма цифр равна n, увеличиваем счетчик
        if digit_sum == n:
            count += 1

    # Выводим результат
print(f"Количество трехзначных чисел, сумма цифр которых равна {n}: {count}")
